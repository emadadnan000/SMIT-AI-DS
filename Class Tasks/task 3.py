# Comparison Operators
a = 10
b = 20

print("a == b:", a == b)  # Equal
print("a != b:", a != b)  # Not equal
print("a > b:", a > b)    # Greater than
print("a < b:", a < b)    # Less than
print("a >= b:", a >= b)  # Greater than or equal to
print("a <= b:", a <= b)  # Less than or equal to

# Logical Operators
x = True
y = False

print("x and y:", x and y)  # Logical AND
print("x or y:", x or y)    # Logical OR
print("not x:", not x)      # Logical NOT

# Membership Operators
my_list = [1, 2, 3, 4, 5]
print("3 in my_list:", 3 in my_list)      # Membership test (in)
print("6 in my_list:", 6 in my_list)      # Membership test (in)
print("6 not in my_list:", 6 not in my_list)  # Membership test (not in)