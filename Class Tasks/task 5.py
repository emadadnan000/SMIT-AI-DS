#Convert a given string to uppercase and lowercase.
str1 = "Python"
print(str1.upper())
print(str1.lower())
#Extract a specific substring from a given string.
str2 = "Sample Text"
print(str2[7:11])
#Reverse a given string using slicing.
str3 = "Pythonic"
