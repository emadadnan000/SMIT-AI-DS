stp = "123456789"
print(len(stp))
print(stp[0]) # print the numbers index
#Slicing 
# starting index is included whereas ending index is not included 
# third arg is for step
print(stp[1:4])   # print the numbers from index 1 to 4
print(stp[0:6:2])
# reverse indexing
print(stp[-1])
# reverse slicing   
print(stp[-1:-6:-1])
#reverse the string
print(stp[::-1])
